忆秦娥·娄山关
现代 毛泽东
西风烈，长空雁叫霜晨月。
霜晨月，马蹄声碎，喇叭声咽。

雄关漫道真如铁，而今迈步从头越。
从头越，苍山如海，残阳如血。


钱塘湖春行
唐 白居易
孤山寺北贾亭西，水面初平云脚低。
几处早莺争暖树，谁家新燕啄春泥。
乱花渐欲迷人眼，浅草才能没马蹄。
最爱湖东行不足，绿杨阴里白沙堤。


望岳
唐 杜甫
岱宗夫如何？齐鲁青未了。
造化钟神秀，阴阳割昏晓。
荡胸生曾云，决眦入归鸟。
会当凌绝顶，一览众山小。


春望
唐 杜甫
国破山河在，城春草木深。
感时花溅泪，恨别鸟惊心。
烽火连三月，家书抵万金。
白头搔更短，浑欲不胜簪。


蜀相
唐 杜甫
丞相祠堂何处寻，锦官城外柏森森。
映阶碧草自春色，隔叶黄鹂空好音。
三顾频烦天下计，两朝开济老臣心。
出师未捷身先死，长使英雄泪满襟。


使至塞上
唐 王维
单车欲问边，属国过居延。
征蓬出汉塞，归雁入胡天。
大漠孤烟直，长河落日圆。
萧关逢候骑，都护在燕然。


酬乐天扬州初逢席上见赠
唐 刘禹锡
巴山楚水凄凉地，二十三年弃置身。
怀旧空吟闻笛赋，到乡翻似烂柯人。
沉舟侧畔千帆过，病树前头万木春。
今日听君歌一曲，暂凭杯酒长精神。


浣溪沙
宋 晏殊
一曲新词酒一杯，去年天气旧亭台。夕阳西下几时回？

无可奈何花落去，似曾相识燕归来。小园香径独徘徊。


卜算子·送鲍浩然之浙东
宋 王观
水是眼波横，山是眉峰聚。欲问行人去那边？眉眼盈盈处。

才始送春归，又送君归去。若到江南赶上春，千万和春住。


临江仙
明 杨慎
滚滚长江东逝水，浪花淘尽英雄。
是非成败转头空。青山依旧在，几度夕阳红。

白发渔樵江渚上，惯看秋月春风。
一壶浊酒喜相逢。古今多少事，都付笑谈中。


满江红·怒发冲冠
宋 岳飞
怒发冲冠，凭阑处、潇潇雨歇。
抬望眼，仰天长啸，壮怀激烈。
三十功名尘与土，八千里路云和月。
莫等闲，白了少年头，空悲切。

靖康耻，犹未雪；臣子恨，何时灭？
驾长车，踏破贺兰山缺。
壮志饥餐胡虏肉，笑谈渴饮匈奴血。
待从头，收拾旧山河，朝天阙。


破阵子·为陈同甫赋壮词以寄之  
宋 辛弃疾
醉里挑灯看剑，梦回吹角连营。
八百里分麾下炙，五十弦翻塞外声，沙场秋点兵。

马作的卢飞快，弓如霹雳弦惊。
了却君王天下事，赢得生前身后名。可怜白发生！


念奴娇·赤壁怀古
宋 苏轼
大江东去，浪淘尽，千古风流人物。
故垒西边，人道是，三国周郎赤壁。
乱石穿空，惊涛拍岸，卷起千堆雪。
江山如画，一时多少豪杰。

遥想公瑾当年，小乔初嫁了，雄姿英发。
羽扇纶巾，谈笑间，樯橹灰飞烟灭。
故国神游，多情应笑我，早生华发。
人生如梦，一尊还酹江月。
