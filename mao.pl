银瓶乍破水浆迸，铁骑突出刀枪鸣。曲终收拨当心画，四弦一声如裂帛。
东船西舫悄无言，唯见江心秋月白。沉吟放拨插弦中，整顿衣裳起敛容。
自言本是京城女，家在虾蟆陵下住。十三学得琵琶成，名属教坊第一部。
曲罢曾教善才服，妆成每被秋娘妒。五陵年少争缠头，一曲红绡不知数。
钿头银篦击节碎，血色罗裙翻酒污。今年欢笑复明年，秋月春风等闲度。
弟走从军阿姨死，暮去朝来颜色故。门前冷落鞍马稀，老大嫁作商人妇。
