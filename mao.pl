五月榴花照眼明，枝间时见子初成。
可怜此地无车马，颠倒苍苔落绛英。


绝句·古木阴中系短篷  
宋 志南
古木阴中系短篷，杖藜扶我过桥东。
沾衣欲湿杏花雨，吹面不寒杨柳风。


登飞来峰
宋  王安石
飞来山上千寻塔，闻说鸡鸣见日升。
不畏浮云遮望眼，自缘身在最高层。


月下独酌
唐  李白
花间一壶酒，独酌无相亲。举杯邀明月，对影成三人。
月既不解饮，影徒随我身。暂伴月将影，行乐须及春。
我歌月徘徊，我舞影零乱。醒时相交欢，醉后各分散。
永结无情游，相期邈云汉。


次北固山下
唐 王湾
客路青山外，行舟绿水前。
潮平两岸阔，风正一帆悬。
海日生残夜，江春入旧年。
乡书何处达？归雁洛阳边。


沁园春·雪
现代 毛泽东
北国风光，千里冰封，万里雪飘。
望长城内外，惟余莽莽；大河上下，顿失滔滔。
山舞银蛇，原驰蜡象，欲与天公试比高。

须晴日，看红装素裹，分外妖娆。
江山如此多娇，引无数英雄竞折腰。
惜秦皇汉武，略输文采；唐宗宋祖，稍逊风骚。
一代天骄，成吉思汗，只识弯弓射大雕。
俱往矣，数风流人物，还看今朝。


